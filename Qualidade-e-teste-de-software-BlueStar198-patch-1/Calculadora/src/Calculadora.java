 public class Calculadora {
    /**
     * Calcula a soma de dois inteiros.
     * @param a O primeiro inteiro.
     * @param b O segundo inteiro.
     * @return A soma de a e b.
     */
    public int Somar(int a, int b){ // Calculo de Soma
        return a + b;
    }
    
    /**
     * Calcula a subtração de dois inteiros.
     * @param a O primeiro inteiro.
     * @param b O segundo inteiro.
     * @return A subtração de a por b.
     */
    public int Subtrair(int a, int b){ // Calculo de Subtração
        return a - b;
    }
    
    /**
     * Calcula a multiplicação de dois inteiros.
     * @param a O primeiro inteiro.
     * @param b O segundo inteiro.
     * @return A multiplicação de a por b.
     */
    public int Multiplicar(int a, int b){ // Calculo de Multiplicação
        return a * b;
    }
    
    /**
     * Calcula a divisão de dois inteiros.
     * @param a O dividendo.
     * @param b O divisor.
     * @return A Divisão de a por b.
     */
    public double Dividir(int a, int b){ // Calculo de Divisão
        return a / b;
    }
}
